/*
 * - 자바는 클래스 기반 언어 ==> 클래스 안에서만 코드를 작성
 *   public class 클래스명 {
 *   
 *   }
 * 	
 * - 메인 메서드(method) : 메인함수
 *   : 프로그램의 메인기능
 *     => 없으면 프로그램 실행 안함
 *   : 프로그램의 시작점 + 종료점
 *   
 *   메인함수() { <= 프로그램 시작
 *   
 *   } <= 프로그램 종료
 * 
 *   : 반드시 1개만 있어야 함
 */


public class Test01 {
	public static void main(String[] args) {
		System.out.println("나의 첫 자바!");
		// 저장 : ctrl + s
		// 실행 : ctrl + F11
		System.out.println("ㅋㅋㅋㅋㅋ");
		System.out.println(10 * 2.5);
	} // main() 
	
} // Test01 영역

